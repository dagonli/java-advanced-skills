package com.upcredit.utils.data;

public class Address {
    private String houseName;
    private String houseNumber;
    private String flatNumber;
    private String streetName;
    private String townName;
    private String countryName;
    private String postcode;
    private int yearsAtAddress;
    private int monthsAtAddress;
    private String moveInDate;

    public String getHouseName() {
        return houseName;
    }

    public void setHouseName(String houseName) {
        this.houseName = houseName;
    }

    public String getHouseNumber() {
        return houseNumber;
    }

    public void setHouseNumber(String houseNumber) {
        this.houseNumber = houseNumber;
    }

    public String getFlatNumber() {
        return flatNumber;
    }

    public void setFlatNumber(String flatNumber) {
        this.flatNumber = flatNumber;
    }

    public String getStreetName() {
        return streetName;
    }

    public void setStreetName(String streetName) {
        this.streetName = streetName;
    }

    public String getTownName() {
        return townName;
    }

    public void setTownName(String townName) {
        this.townName = townName;
    }

    public String getCountryName() {
        return countryName;
    }

    public void setCountryName(String countryName) {
        this.countryName = countryName;
    }

    public String getPostcode() {
        return postcode;
    }

    public void setPostcode(String postcode) {
        this.postcode = postcode;
    }

    public int getYearsAtAddress() {
        return yearsAtAddress;
    }

    public void setYearsAtAddress(int yearsAtAddress) {
        this.yearsAtAddress = yearsAtAddress;
    }

    public int getMonthsAtAddress() {
        return monthsAtAddress;
    }

    public void setMonthsAtAddress(int monthsAtAddress) {
        this.monthsAtAddress = monthsAtAddress;
    }

    public String getMoveInDate() {
        return moveInDate;
    }

    public void setMoveInDate(String moveInDate) {
        this.moveInDate = moveInDate;
    }
// Getters and Setters
}